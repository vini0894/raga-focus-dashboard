def _candidate_cluster(candidate):
    """Return the audience cluster of a candidate (e.g., 'evening_serene')."""
    p = candidate["components"]["problem"]["kw"].lower()
    for key, cluster in PROBLEM_TO_RAGA_MOOD.items():
        if key in p:
            return cluster
    return "unclassified"


def _dominant_cluster_of(catalog, days=5):
    """Find the cluster covered most often in the last N days of own videos."""
    from datetime import date as _date
    counts = {}
    today = _date.today()
    for v in catalog[:10]:
        days_ago = (today - v["publish_date"]).days
        if days_ago > days:
            continue
        title_lower = v["title"].lower()
        for key, cluster in PROBLEM_TO_RAGA_MOOD.items():
            if key in title_lower:
                counts[cluster] = counts.get(cluster, 0) + 1
                break
    if not counts:
        return None, {}
    dominant = max(counts.items(), key=lambda x: x[1])[0]
    return dominant, counts


def split_candidates_by_cluster(candidates, catalog):
    """Return (double_down_pick, diversify_pick, others) — top per track."""
    dominant_cluster, cluster_counts = _dominant_cluster_of(catalog)
    in_dominant = []
    out_dominant = []
    for c in candidates:
        cluster = _candidate_cluster(c)
        c["_cluster"] = cluster
        if dominant_cluster and cluster == dominant_cluster:
            in_dominant.append(c)
        else:
            out_dominant.append(c)
    double_down = in_dominant[0] if in_dominant else None
    diversify   = out_dominant[0] if out_dominant else None
    others = [c for c in candidates if c is not double_down and c is not diversify][:3]
    return double_down, diversify, others, dominant_cluster, cluster_counts


